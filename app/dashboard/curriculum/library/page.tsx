'use client';

import { useQuery } from '@tanstack/react-query';
import api from '@/lib/api';
import { useState } from 'react';
import { Search, Filter, CheckCircle, Clock, Target, BookOpen } from 'lucide-react';

export default function ActivityLibraryPage() {
  const [filters, setFilters] = useState({
    teacherApproved: '',
    generatedBy: '',
    minAlignment: '',
  });
  const [searchTerm, setSearchTerm] = useState('');

  const { data: activities, isLoading } = useQuery({
    queryKey: ['supercurriculum-activities', filters],
    queryFn: () => api.getSupercurriculumActivities(filters),
  });

  const { data: years } = useQuery({
    queryKey: ['years'],
    queryFn: () => api.getYears(),
  });

  const { data: subjects } = useQuery({
    queryKey: ['subjects'],
    queryFn: () => api.getSubjects(),
  });

  const filteredActivities = activities?.filter((activity: any) =>
    activity.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
    activity.curriculumTopic.topicName.toLowerCase().includes(searchTerm.toLowerCase())
  );

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-64">
        <div className="text-gray-500">Loading activity library...</div>
      </div>
    );
  }

  return (
    <div>
      <div className="mb-8">
        <h1 className="text-3xl font-bold text-gray-900">Activity Library</h1>
        <p className="text-gray-500 mt-1">Browse all supercurriculum activities</p>
      </div>

      {/* Filters */}
      <div className="bg-white rounded-lg shadow p-6 mb-6">
        <div className="grid grid-cols-1 md:grid-cols-4 gap-4">
          {/* Search */}
          <div className="md:col-span-2">
            <label className="block text-sm font-medium text-gray-700 mb-2">
              <Search className="inline h-4 w-4 mr-1" />
              Search
            </label>
            <input
              type="text"
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              placeholder="Search by title or topic..."
              className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-indigo-500"
            />
          </div>

          {/* Approval Status */}
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-2">Approval Status</label>
            <select
              value={filters.teacherApproved}
              onChange={(e) => setFilters({ ...filters, teacherApproved: e.target.value })}
              className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-indigo-500"
            >
              <option value="">All</option>
              <option value="true">Approved</option>
              <option value="false">Pending</option>
            </select>
          </div>

          {/* Generated By */}
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-2">Generated By</label>
            <select
              value={filters.generatedBy}
              onChange={(e) => setFilters({ ...filters, generatedBy: e.target.value })}
              className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-indigo-500"
            >
              <option value="">All</option>
              <option value="AI_AGENT">AI Agent</option>
              <option value="TEACHER">Teacher</option>
              <option value="HYBRID">Hybrid</option>
            </select>
          </div>
        </div>
      </div>

      {/* Stats */}
      <div className="grid grid-cols-1 md:grid-cols-4 gap-4 mb-6">
        <StatCard
          label="Total Activities"
          value={activities?.length || 0}
          icon={BookOpen}
          color="bg-blue-500"
        />
        <StatCard
          label="Approved"
          value={activities?.filter((a: any) => a.teacherApproved).length || 0}
          icon={CheckCircle}
          color="bg-green-500"
        />
        <StatCard
          label="Pending"
          value={activities?.filter((a: any) => !a.teacherApproved).length || 0}
          icon={Clock}
          color="bg-yellow-500"
        />
        <StatCard
          label="Avg Alignment"
          value={`${(activities?.reduce((sum: number, a: any) => sum + a.curriculumAlignment, 0) / (activities?.length || 1) || 0).toFixed(0)}%`}
          icon={Target}
          color="bg-indigo-500"
        />
      </div>

      {/* Activities Grid */}
      {filteredActivities && filteredActivities.length > 0 ? (
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {filteredActivities.map((activity: any) => (
            <ActivityCard key={activity.id} activity={activity} />
          ))}
        </div>
      ) : (
        <div className="bg-white rounded-lg shadow p-12 text-center">
          <Filter className="h-16 w-16 text-gray-400 mx-auto mb-4" />
          <h3 className="text-xl font-semibold text-gray-900 mb-2">No activities found</h3>
          <p className="text-gray-500">Try adjusting your filters</p>
        </div>
      )}
    </div>
  );
}

function ActivityCard({ activity }: { activity: any }) {
  return (
    <div className="bg-white border border-gray-200 rounded-lg p-6 hover:shadow-lg transition-shadow">
      <div className="flex items-start justify-between mb-3">
        <h3 className="font-semibold text-gray-900 flex-1 pr-2">{activity.title}</h3>
        {activity.teacherApproved ? (
          <CheckCircle className="h-5 w-5 text-green-500 flex-shrink-0" />
        ) : (
          <Clock className="h-5 w-5 text-yellow-500 flex-shrink-0" />
        )}
      </div>

      <div className="text-sm text-gray-600 mb-3">
        {activity.curriculumTopic.subject.displayName} â€¢ {activity.curriculumTopic.yearGroup.displayName}
      </div>

      <div className="text-sm text-gray-500 mb-4 line-clamp-2">
        {activity.curriculumTopic.topicName}
      </div>

      <div className="flex items-center justify-between">
        <span className={`text-xs px-2 py-1 rounded ${
          activity.extensionLevel === 'BEYOND_CURRICULUM'
            ? 'bg-purple-100 text-purple-700'
            : 'bg-blue-100 text-blue-700'
        }`}>
          {activity.extensionLevel === 'BEYOND_CURRICULUM' ? 'Advanced' : 'Enrichment'}
        </span>
        <div className="flex items-center gap-1 text-xs text-gray-500">
          <Target className="h-3 w-3" />
          {activity.curriculumAlignment}% aligned
        </div>
      </div>
    </div>
  );
}

function StatCard({ label, value, icon: Icon, color }: any) {
  return (
    <div className="bg-white rounded-lg shadow p-4">
      <div className="flex items-center justify-between">
        <div>
          <div className="text-sm text-gray-500 mb-1">{label}</div>
          <div className="text-2xl font-bold text-gray-900">{value}</div>
        </div>
        <div className={`${color} rounded-lg p-3`}>
          <Icon className="h-5 w-5 text-white" />
        </div>
      </div>
    </div>
  );
}

